//switch
//1-sunday, 2 mon, 3- tues, 4 -wed, 5 -thu, 6 -fri, 7 -sat


let day = 3;
switch (day) {
    case 1:
        console.log("sunday");


    case 2:
        console.log("monday");


    case 3:
        console.log("tuesday");


    case 4:
        console.log("wednesday");


    case 5:
        console.log("thursday");


    case 6:
        console.log("friday");


    case 7:
        console.log("saturday");







}