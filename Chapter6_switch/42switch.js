//switch
//1-sunday, 2 mon, 3- tues, 4 -wed, 5 -thu, 6 -fri, 7 -sat


let day = 3;
switch (day) {
    case 1:
        console.log("sunday");
        break;

    case 2:
        console.log("monday");
        break;

    case 3:
        console.log("tuesday");
        break;

    case 4:
        console.log("wednesday");
        break;

    case 5:
        console.log("thursday");
        break;

    case 6:
        console.log("friday");
        break;

    case 7:
        console.log("saturday");
        break;

    default:
        console.log("not a day");

        break;



}